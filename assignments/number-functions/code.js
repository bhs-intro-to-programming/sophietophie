
const add = (n1, n2) => {
  return n1 + n2;
}
const subtract = (n1, n2) => {
  return n1 - n2;
}
const multiply = (n1, n2) => {
  return n1 * n2;
}
const divide = (n1, n2) => {
  return n1 / n2;
}
const mod = (n1, n2) => {
  return n1 % n2;
}
const averageOfTwo = (n1, n2) => {
  return (n1 + n2) / 2;
}
const averageOfThree = (n1, n2, n3) => {
  return (n1 + n2 + n3) / 3;
}
const distance = (n1, n2) => {
  return Math.abs(n1 - n2);
}
const manhattanDistance = (x1, y1, x2, y2) => {
  return Math.abs(x1 - x2) + (y1 - y2);
}